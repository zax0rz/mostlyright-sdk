function emitDataBarrels(out: FileMap): void {
  const coreBarrel = `${header("(barrel)")}\nexport * from \"./stations.js\";\nexport * from \"./source-priority.js\";\nexport * from \"./qc-alpha-rules.js\";\n`;
  emit(out, join(PACKAGES_TS, "core", "src", "data", "generated", "index.ts"), coreBarrel);

  const marketsBarrel = `${header("(barrel)")}\nexport * from \"./kalshi-stations.js\";\nexport * from \"./polymarket-city-stations.js\";\n`;
  emit(out, join(PACKAGES_TS, "markets", "src", "data", "generated", "index.ts"), marketsBarrel);
}

