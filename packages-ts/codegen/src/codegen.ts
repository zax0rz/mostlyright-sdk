// @mostlyrightmd/codegen — CLI entry point.
//
// Reads canonical schema artifacts from <repoRoot>/schemas/ and emits typed
// TS interfaces + typed data modules into the consumer packages under
// packages-ts/*/src/{schemas,data}/generated/.
//
// Usage:
//   pnpm --filter @mostlyrightmd/codegen run codegen          # emit files
//   pnpm --filter @mostlyrightmd/codegen run codegen --check  # determinism check
//
// Per TS-W0 Wave 3, ajv-standalone validator emission is DEFERRED to TS-W3
// (TS-VALIDATOR-01) — a placeholder file is emitted in its place.

import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

import Ajv from "ajv/dist/2020.js";
import standaloneCode from "ajv/dist/standalone/index.js";
import { compile } from "json-schema-to-typescript";

// -------------------------------------------------------------------------
// Path resolution
// -------------------------------------------------------------------------

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

/** Walk up from `start` until we find a directory containing `marker`. */
function findRepoRoot(start: string, marker = "pnpm-workspace.yaml"): string {
  let cur = resolve(start);
  while (true) {
    if (existsSync(join(cur, marker))) return cur;
    const parent = dirname(cur);
    if (parent === cur) {
      throw new Error(
        `Could not find ${marker} walking up from ${start}. Codegen must be run inside the mostlyright repository.`,
      );
    }
    cur = parent;
  }
}

const REPO_ROOT = findRepoRoot(__dirname);
const SCHEMAS_DIR = join(REPO_ROOT, "schemas");
const PACKAGES_TS = join(REPO_ROOT, "packages-ts");

// -------------------------------------------------------------------------
// Header (3-line "AUTO-GENERATED" banner)
// -------------------------------------------------------------------------

function header(sourceRelPath: string): string {
  return `// AUTO-GENERATED by @mostlyrightmd/codegen from schemas/${sourceRelPath}.\n// DO NOT EDIT — regenerate with: pnpm codegen\n// Last manifest SHA recorded in schemas/EXPORT_MANIFEST.json\n`;
}

// -------------------------------------------------------------------------
// Determinism helpers
// -------------------------------------------------------------------------

/** Stable JSON stringify with sorted keys. */
function stableStringify(value: unknown, indent = 2): string {
  const seen = new WeakSet();
  const sort = (v: unknown): unknown => {
    if (v === null || typeof v !== "object") return v;
    if (seen.has(v as object)) return v;
    seen.add(v as object);
    if (Array.isArray(v)) return v.map(sort);
    const sorted: Record<string, unknown> = {};
    for (const k of Object.keys(v as Record<string, unknown>).sort()) {
      sorted[k] = sort((v as Record<string, unknown>)[k]);
    }
    return sorted;
  };
  return JSON.stringify(sort(value), null, indent);
}

/** Serialize a TS literal value. Strings/numbers/null/arrays/objects only. */
function tsLiteral(value: unknown, indent = 0, indentStep = 2): string {
  const pad = " ".repeat(indent);
  const padNext = " ".repeat(indent + indentStep);
  if (value === null) return "null";
  if (typeof value === "string") return JSON.stringify(value);
  if (typeof value === "number" || typeof value === "boolean") return String(value);
  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    const items = value.map((v) => `${padNext}${tsLiteral(v, indent + indentStep, indentStep)}`);
    return `[\n${items.join(",\n")},\n${pad}]`;
  }
  if (typeof value === "object") {
    const obj = value as Record<string, unknown>;
    const keys = Object.keys(obj).sort();
    if (keys.length === 0) return "{}";
    const parts = keys.map((k) => {
      const safeKey = /^[A-Za-z_$][A-Za-z0-9_$]*$/.test(k) ? k : JSON.stringify(k);
      return `${padNext}${safeKey}: ${tsLiteral(obj[k], indent + indentStep, indentStep)}`;
    });
    return `{\n${parts.join(",\n")},\n${pad}}`;
  }
  throw new Error(`Unserialisable value: ${String(value)}`);
}

// -------------------------------------------------------------------------
// File I/O helpers (in-memory map for --check mode)
// -------------------------------------------------------------------------

type FileMap = Map<string, string>;

function emit(out: FileMap, absPath: string, contents: string): void {
  // Always normalize to LF + single trailing newline.
  let body = contents.replace(/\r\n/g, "\n");
  if (!body.endsWith("\n")) body += "\n";
  out.set(absPath, body);
}

function writeFiles(files: FileMap): void {
  for (const [path, body] of files) {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, body, "utf8");
  }
}

