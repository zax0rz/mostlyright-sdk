interface RawQcRule {
  rule_id: string;
  bit_position: number;
  description: string;
  field: string;
}

function emitQcRules(out: FileMap): void {
  const absSrc = join(SCHEMAS_DIR, "qc-alpha-rules.json");
  const parsed = JSON.parse(readFileSync(absSrc, "utf8")) as Record<string, unknown>;

  if (parsed.gated === true) {
    const reason = typeof parsed.reason === "string" ? parsed.reason : "";
    const body = `${header("qc-alpha-rules.json")}\nexport const QC_ALPHA_RULES_GATED = true as const;\nexport const QC_ALPHA_RULES_REASON = ${JSON.stringify(reason)};\n`;
    emit(out, join(PACKAGES_TS, "core", "src", "data", "generated", "qc-alpha-rules.ts"), body);
    return;
  }

  const rules = parsed.rules as RawQcRule[];
  // Sort by bit_position for determinism (already typically ordered).
  const sorted = [...rules].sort((a, b) => a.bit_position - b.bit_position);
  const rulesLit = tsLiteral(sorted);
  const mapEntries = sorted
    .map((r, i) => `  [${JSON.stringify(r.rule_id)}, QC_ALPHA_RULES[${i}]!],`)
    .join("\n");

  const body = `${header("qc-alpha-rules.json")}\nexport interface QcAlphaRule {\n  rule_id: string;\n  bit_position: number;\n  description: string;\n  field: string;\n}\n\nexport const QC_ALPHA_RULES: ReadonlyArray<QcAlphaRule> = ${rulesLit} as const;\n\nexport const QC_ALPHA_RULES_BY_ID: ReadonlyMap<string, QcAlphaRule> = new Map<string, QcAlphaRule>([\n${mapEntries}\n]);\n`;

  emit(out, join(PACKAGES_TS, "core", "src", "data", "generated", "qc-alpha-rules.ts"), body);
}

