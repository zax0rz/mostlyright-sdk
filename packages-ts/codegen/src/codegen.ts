async function generateAll(): Promise<FileMap> {
  const files: FileMap = new Map();
  await emitSchemas(files);
  emitStations(files);
  emitSourcePriority(files);
  emitQcRules(files);
  emitKalshi(files);
  emitPolymarket(files);
  emitDataBarrels(files);
  return files;
}

async function main(): Promise<void> {
  const args = process.argv.slice(2);
  const checkMode = args.includes("--check");

  if (checkMode) {
    const a = await generateAll();
    const b = await generateAll();
    if (a.size !== b.size) {
      console.error(`Non-determinism: run 1 emitted ${a.size} files; run 2 emitted ${b.size}.`);
      process.exit(1);
    }
    for (const [path, body] of a) {
      const other = b.get(path);
      if (other === undefined) {
        console.error(`Non-determinism: ${path} only present in run 1.`);
        process.exit(1);
      }
      if (other !== body) {
        console.error(`Non-determinism: ${path} differs between runs.`);
        process.exit(1);
      }
    }
    console.log(`codegen --check: ${a.size} files byte-equal across two runs. OK.`);
    return;
  }

  const files = await generateAll();
  writeFiles(files);
  console.log(`codegen: wrote ${files.size} generated files under packages-ts/`);
  for (const path of [...files.keys()].sort()) {
    const rel = path.startsWith(REPO_ROOT) ? path.slice(REPO_ROOT.length + 1) : path;
    console.log(`  ${rel}`);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
